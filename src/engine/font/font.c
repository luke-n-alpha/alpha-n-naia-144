static int table_lookup(uint32_t cp, const uint16_t *table, int count) {
    int i;
    for (i = 0; i < count; i++) {
        if (table[i] == cp) return i;
    }
    return -1;
}

static int cp_to_index(uint32_t cp) {
    int idx;
    if (cp >= GLYPH_ASCII_START && cp < GLYPH_ASCII_START + GLYPH_ASCII_COUNT)
        return cp - GLYPH_ASCII_START;

    idx = table_lookup(cp, JAMO_CODEPOINTS, GLYPH_JAMO_COUNT);
    if (idx >= 0) return GLYPH_ASCII_COUNT + idx;

    idx = table_lookup(cp, BOX_CODEPOINTS, GLYPH_BOX_COUNT);
    if (idx >= 0) return GLYPH_BOX_INDEX_START + idx;

    return -1;
}

const GlyphBitmap *font_get_glyph(uint32_t codepoint) {
    int idx = cp_to_index(codepoint);
    if (idx >= 0) return &FONT_GLYPHS[idx];
    return &FONT_GLYPHS[0];
}
